// assignment operator
let r = 5;

// addition assignment
r = r + 1;
r += 1;

// subtraction assignment
r = r - 1;
r -= 1;

// multiplication assignment
r = r * 1;
r *= 1;

// division assignment
r = r / 1;
r /= 1;

// remainder (modulus) assignment - gives the remainder of division
r = r % 1;
r %= 1;

// exponential assignment - multiplies variable by itself however many times defined
r = r ** 1;
r **= 1;