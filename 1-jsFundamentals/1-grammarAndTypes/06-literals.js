// literals are simply just values that we as developers add to our code

// string literal - anything between double or single quotes
let car = "Ford";

// numeric literal
let december = 12;

// boolean literal
let tired = false;

// object literal - uses dot notation 
let soup = {
    a: "chicken noodle",
    b: "tomato",
    c: "beef and barley"
}

console.log(soup.a);

// array literal
let days = ["Monday", "Tuesday", "Wednesday", "THursday", "Friday", "Saturday", "Sunday"];

console.log(days.toString());
console.log(days.length);